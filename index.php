      <h3>UDUAL ( Unión de Universidades de América Latina y el Caribe)</h3>
      <p>La Universidad forma parte de la organización de universidades e instituciones de educación superior de la región, encaminada al propósito de establecer lazos firmes de cooperación, en un marco de respeto y pluralidad, con un claro compromiso de vinculación social de la educación superior con países como: Argentina, Bolivia, Brasil, Chile, Colombia, Costa Rica, Ecuador, El Salvador, España, Nicaragua, Panamá, Perú, Paraguay y Uruguay.</p>
      <p>Somos partícipes del Programa Académico de Movilidad Educativa (PAME) es una iniciativa de la UDUAL que permite realizar movilidad entre las Instituciones de Educación Superior (IES) afiliadas bajo el principio de reciprocidad. A partir del año 2006, se vuelve un programa permanente que busca favorecer los procesos de internacionalización.</p>

