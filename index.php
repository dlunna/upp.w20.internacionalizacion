      <h3>Secretaría de Relaciones Exteriores</h3>
      <p>Nuestra institución está registrada en las 90 Instituciones de Educación Superior (IES) mexicanas participan en esta Convocatoria y todas cuentan con programas académicos registrados en el Programa Nacional de Posgrados de Calidad del Consejo Nacional de Ciencia y Tecnología.</p>
      <p>Participando en el programa Becas a extranjeros otorgadas por la Agencia Mexicana de Cooperación Internacional para el Desarrollo (AMEXCID) se ofrece a más de 180 países, en el marco de convenios bilaterales, de mecanismos multilaterales y de acuerdos especiales. </p>

      <h3>Alianza sel Pacífico</h3>
      <p>La Universidad Politécnica de Pachuca pertenece a la Alianza del Pacífico, la cual nació como una iniciativa económica y de desarrollo entre cuatro naciones de América Latina: Chile, Colombia, México y Perú. Así, se ha convertido en el eje de una nueva forma de hacer negocios en el continente.</p>
      <p>Espacio de cooperación efectivo que impulsa iniciativas innovadoras sobre:</p>
